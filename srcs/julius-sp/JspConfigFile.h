#ifndef _JSP_CONFIG_FILE_H_
#define _JSP_CONFIG_FILE_H_

#include <string.h>

#include <string>
#include <vector>
#include <map>

namespace Jsp
{
	// ----------------------------------------------------
	///
	///
	/// @code
	/// KEY1=VALUE1
	/// KEY2=VALUE2
	/// ...
	/// @endcode
	// ----------------------------------------------------
	class ConfigFile
	{
	public:
		// ----------------------------------------------------
		// ----------------------------------------------------
		ConfigFile();

		// ----------------------------------------------------
		// ----------------------------------------------------
		~ConfigFile();

		// ----------------------------------------------------
		///
		// ----------------------------------------------------
		bool load(const char *configFile);

		// ----------------------------------------------------
		///
		// ----------------------------------------------------
		const char *getStringValue(const char *name);

		// ----------------------------------------------------
		///
		// ----------------------------------------------------
		static std::string trim(std::string str);

		// ----------------------------------------------------
		///
		// ----------------------------------------------------
		static std::string stripNLAtLineEnd(std::string str);

	private:
		std::map<std::string, std::string> m_values;
	};
};

#endif

